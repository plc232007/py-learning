numb1 = int(input("Informe o primeiro número: "))
numb2 = int(input("Informe o segundo número: "))
operador = input ("Informe o operador: ")

if (operador == '+') :

    print (f'{numb1} {operador} {numb2} = {numb1 + numb2}')

elif (operador == '-') :
    print (f'{numb1} {operador} {numb2} = {numb1 - numb2}')

elif (operador == '*') :
    print (f'{numb1} {operador} {numb2} = {numb1 * numb2}')
    
elif (operador == '/') :
    print (f'{numb1} {operador} {numb2} = {numb1 / numb2}')










